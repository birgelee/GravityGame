package javathing.statics;

/**
 *
 * @author Henry
 */
public class LevelVariables {
    
    private int levelNumber;
    private int maxLevelNumber;

    /**
     * @return the levelNumber
     */
    public int getLevelNumber() {
	return levelNumber;
    }

    /**
     * @param levelNumber the levelNumber to set
     */
    public void setLevelNumber(int levelNumber) {
	this.levelNumber = levelNumber;
    }

    /**
     * @return the maxLevelNumber
     */
    public int getMaxLevelNumber() {
	return maxLevelNumber;
    }

    /**
     * @param maxLevelNumber the maxLevelNumber to set
     */
    public void setMaxLevelNumber(int maxLevelNumber) {
	this.maxLevelNumber = maxLevelNumber;
    }
    
}
